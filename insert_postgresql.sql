    age_group,
    RANK() OVER (ORDER BY AVG(urinalysis_id) DESC) AS rank,
    DENSE_RANK() OVER (ORDER BY AVG(urinalysis_id) DESC) AS dense_rank,
    COUNT(patient_id) AS patient_count
FROM
    age_groups
GROUP BY
    age_group
ORDER BY
rank;


